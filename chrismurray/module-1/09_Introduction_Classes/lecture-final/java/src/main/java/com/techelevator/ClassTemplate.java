package com.techelevator;

// import java.util.*;

public class ClassTemplate {

	// ------------------------ CLASS (INSTANCE) VARIABLES ------------------------
	
	/* ***************************************************************************************
	 * Private Variables (or Members)
	 * ***************************************************************************************
	 * These are values that we have decided should be controlled internally through Accessors 
	 *  (get and set). This keeps other classes from changing them in way that may break our 
	 *   internal logic or processes. This can happen if values are not validated, for example.
	 ************************************************************************************** */

	// ------------------------ PROPERTIES (OR ATTRIBUTES) ------------------------
	
	// ***************************************************************************************
	// Public Properties (or Members)
	// ***************************************************************************************
	// These are the properties that we have determined are OK for external actors to access 
	//   and either get or set their value. If left exposed we have little control over what
	//   what goes into them and the way that erroneous input will affect our dependent 
	//	 internal processes.
	// ***************************************************************************************

	
	
	// ***************************************************************************************
	// Derived Properties
	// ***************************************************************************************
	// These are the properties that we expose through methods. Derived properties are methods 
	//   that return properties that do not need to be stored internally because they can derived 
	//   from one or more existing properties. Derived properties will make use of class members 
	//   using the "this." keyword to create a derived property.
	// ***************************************************************************************	
	
	
	
	// ***************************************************************************************
	// Accessors: Gettor and settor methods
	// ***************************************************************************************	
	// These are methods that allow us to control the access to private properties. They are 
	//   always preceded by lower case get and set as part of their camel cased names. See
	//   private members above for additional information. 
	// ***************************************************************************************	
	
	// ------------------------ CONSTRUCTOR(S) ------------------------
	
	// ***************************************************************************************
	// CTOR - Constructor(s)
	// ***************************************************************************************
	// Constructors defined how classes are instantiated as Objects. They may have no parameters
	//   (a.k.a the default constructor) or take parameters than can be used to set a default
	//   state. They can also be omitted, in which case a default constructor will be assumed
	//   to exist by the compiler.
	// ***************************************************************************************

	// ------------------------ METHODS ------------------------
	
	// ***************************************************************************************
	// Public Methods
	// ***************************************************************************************
	//   Methods (which encompass functions) do work. Methods defined as void do not return
	//   anything to the caller and typically do work internally. These are typically public or
	//   protected.
	// ***************************************************************************************
	
	// ***************************************************************************************
	// Private Methods
	// ***************************************************************************************
	//   Methods (which encompass functions) do work. Methods defined as void do not return
	//   anything to the caller and typically do work internally. These are typically
	//   private.
	// ***************************************************************************************	


	
}
